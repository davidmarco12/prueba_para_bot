def procesar_lista(items):
    """Procesa lista duplicando cada elemento"""
    resultado = []
    for item in items:
        resultado.append(item * 2)
    return resultado

def transformar_elementos(elementos):
    """Transforma elementos duplicando cada uno"""
    nueva_lista = []
    for elemento in elementos:
        nueva_lista.append(elemento * 2)
    return nueva_lista